package StringPractice;

public class StringConcat {

	public static void main(String[] args) {
	    // By using + operator
		String s1="Sharad ";
		String s2="Pokale";		
        System.out.println(s1+"10"+s2);
        // By using concat() method
        System.out.println(s1.concat(s2)+"10");
        System.out.println();
	}

}
