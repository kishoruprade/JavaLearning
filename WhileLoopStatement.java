package loopssession3;

public class WhileLoopStatement {

	public static void main(String[] args) {
		int i = 1;            //initialization
		while (i<=10){        //condition
			System.out.println(i);
			i = i+1;         //increment and decrement
				}
		System.out.println("**********");
	/////for loop 
		 for (int j = 1; j <=10;j++){        //intialization/condition/increment
			 System.out.println(j);
		 }
		 System.out.println("**********"); 
		// for loop reverse order
		 for (int  k = 10; k >=  -10;k--){     //intialization/condition/decrement
			 System.out.print(k + " ");
			 
		 }
	}
}

