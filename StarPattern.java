
public class StarPattern {

	public static void main(String[] args) {
		for (int i =1;i<=5;i++){
			for (int j=i;j>0;j--){
				System.out.print("*");
				
			}
			System.out.println();
		}
		
		for (int k=5;k>0;k--){
			for (int i=1;i<=0;i++){
				System.out.print(i);
			}
			System.out.println();
		}

	}

}
