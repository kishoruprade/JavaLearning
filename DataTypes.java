// Printing the data types
package firstPassLogicBuilding;

public class DataTypes {

	public static void main(String[] args) {
		byte marks=80;
		System.out.println("Marks are: "+marks);    //Printing the byte value
		
		short jeansPrice=2550;
		System.out.println("Cost of jeans: "+jeansPrice);  //Printing the short value
		
		int hallRent=25000;
        System.out.println("Rent of the marriage hall: "+hallRent);   //Printing the integer value
        
        long landLineNo=912034545;
        System.out.println("Land-line number is: "+landLineNo);     //Printing the long value
        
        float rateOfInterest=7.8f;
        System.out.println("Rate of interest for SBI bank is: "+rateOfInterest);  //Printing the float value
        
        double goldWeight=1.23455667;
        System.out.println("Gold weight: "+goldWeight);                  //Printing the double value
        
        char firstLetter='S';
        System.out.println("First letter of Sharad: "+firstLetter);      //Printing the character value
        
        boolean maritalStatus=true;
        System.out.println("Are you single?-"+maritalStatus);            //Printing the boolean value
        
        
        
	}

}
