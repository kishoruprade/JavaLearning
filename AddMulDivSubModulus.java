package math;

public class AddMulDivSubModulus {

	public static void main(String[] args) {
	int a = 3;
	int b = 5;
	
	System.out.println("First integer: "+a);
	System.out.println("Second integer: "+b);
	
	int sum = a+b;
	System.out.println("Addition of two integers: "+sum);
	
	int subs = b-a;
	System.out.println("Substraction of two integers: "+ subs);
	
	int mul = a*b;
	System.out.println("Multiplication of two integers: "+mul);
	
	int div = 5/3;
	System.out.println("Division of two integers: "+div);
	
	int modulus = 5%3;
	System.out.println("Modulus of two integers: "+modulus);
	

	}

}
